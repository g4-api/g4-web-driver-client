namespace G4.WebDriver.Remote
{
    /// <summary>
    /// Provides constant values representing different source types for input interactions.
    /// </summary>
    public static class InputSourceTypes
    {
        /// <summary>
        /// Represents the "key" source type.
        /// </summary>
        public const string Key = "key";

        /// <summary>
        /// Represents the "none" source type.
        /// </summary>
        public const string None = "none";

        /// <summary>
        /// Represents the "pointer" source type.
        /// </summary>
        public const string Pointer = "pointer";

        /// <summary>
        /// Represents the "wheel" source type.
        /// </summary>
        public const string Wheel = "wheel";
    }
}
