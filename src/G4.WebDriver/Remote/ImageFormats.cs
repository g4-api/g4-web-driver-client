namespace G4.WebDriver.Remote
{
    /// <summary>
    /// Representations of file formats for saving screenshots.
    /// </summary>
    public static class ImageFormats
    {
        /// <summary>
        /// Bitmap image format.
        /// </summary>
        public const string Bmp = "bmp";

        /// <summary>
        /// Graphics Interchange Format image format.
        /// </summary>
        public const string Gif = "gif";

        /// <summary>
        /// Joint Photgraphic Experts Group image format.
        /// </summary>
        public const string Jpeg = "jpeg";

        /// <summary>
        /// W3C Portable Network Graphics image format.
        /// </summary>
        public const string Png = "png";

        /// <summary>
        /// Tagged Image File Format image format.
        /// </summary>
        public const string Tiff = "tiff";
    }
}
