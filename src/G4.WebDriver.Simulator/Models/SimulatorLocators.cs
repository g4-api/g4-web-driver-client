namespace G4.WebDriver.Models
{
    /// <summary>
    /// Provides a collection of constant string values representing various simulator locators.
    /// These locators are used within the G4 WebDriver simulator for testing purposes.
    /// </summary>
    internal static class SimulatorLocators
    {
        /// <summary>
        /// Represents the "body" element locator.
        /// </summary>
        public const string Body = "body";

        /// <summary>
        /// Represents a locator that causes an exception to be thrown.
        /// </summary>
        public const string Exception = "exception";

        /// <summary>
        /// Represents a locator for a file input element.
        /// </summary>
        public const string File = "file";

        /// <summary>
        /// Represents the currently focused element.
        /// </summary>
        public const string Focused = "focused";

        /// <summary>
        /// Represents a locator that simulates an index for collection elements.
        /// </summary>
        public const string Index = "index";

        /// <summary>
        /// Represents a locator for a generic input element.
        /// </summary>
        public const string Input = "input";

        /// <summary>
        /// Represents a locator that simulates an invalid element state.
        /// </summary>
        public const string InvalidElementState = "invalid-state";

        /// <summary>
        /// Represents a locator that simulates a failed element find.
        /// </summary>
        public const string Negative = "negative";

        /// <summary>
        /// Represents a locator that simulates no elements found.
        /// </summary>
        public const string None = "none";

        /// <summary>
        /// Represents a locator that simulates a null element.
        /// </summary>
        public const string Null = "null";

        /// <summary>
        /// Represents the "option" element locator.
        /// </summary>
        public const string Option = "option";

        /// <summary>
        /// Represents a positive locator that simulates a successful element find.
        /// </summary>
        public const string Positive = "positive";

        /// <summary>
        /// Represents a locator that randomly simulates an existing element.
        /// </summary>
        public const string RandomExists = "random-e";

        /// <summary>
        /// Represents a randomly generated negative locator.
        /// </summary>
        public const string RandomNegative = "random-n";

        /// <summary>
        /// Represents a locator that randomly throws a NoSuchElement exception.
        /// </summary>
        public const string RandomNoSuchElement = "random-k";

        /// <summary>
        /// Represents a locator that randomly simulates a non-existing element.
        /// </summary>
        public const string RandomNotExists = "random-x";

        /// <summary>
        /// Represents a randomly generated null locator.
        /// </summary>
        public const string RandomNull = "random-l";

        /// <summary>
        /// Represents a randomly generated positive locator.
        /// </summary>
        public const string RandomPositive = "random-p";

        /// <summary>
        /// Represents a locator that randomly simulates a stale element.
        /// </summary>
        public const string RandomStale = "random-o";

        /// <summary>
        /// Represents a locator for a select element with options.
        /// </summary>
        public const string SelectElement = "select-element";

        /// <summary>
        /// Represents a locator for a select element with no options.
        /// </summary>
        public const string SelectElementNoOptions = "select-element-no-options";

        /// <summary>
        /// Represents a locator that simulates a stale element reference.
        /// </summary>
        public const string Stale = "stale";

        /// <summary>
        /// Represents a locator for a URL element or address bar.
        /// </summary>
        public const string Url = "url";
    }
}
